import turtle as t
t.speed('fast')
t.hideturtle()

t.pensize(5)
t.pencolor('red')
t.circle(100)

t.penup()
t.goto(150,0)
t.pendown()

t.pencolor('green')
t.circle(100)

t.penup()
t.goto(300,0)
t.pendown()

t.pencolor('black')
t.circle(100)

t.penup()
t.goto(75,-90)
t.pendown()

t.pencolor('yellow')
t.circle(100)

t.penup()
t.forward(150)
t.pendown()

t.pencolor('blue')
t.circle(100)

t.done()
